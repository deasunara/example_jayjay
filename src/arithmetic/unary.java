package arithmetic;

public class unary {
    public static void main(String[] args) {

        int nilai1= 7;
        int nilai2= 9;

//        Sama dengan
         System.out.println(nilai1 == nilai2);

//         Lebih besar
        System.out.println(nilai1 > nilai2);

//        Lebih kecil
        System.out.println(nilai2 < nilai1);

//        Lebih kecil sama dengan
        System.out.println(nilai1 <= nilai2);

//        Lebih besar sama dengan
        System.out.println(nilai2 >= nilai1);

//        Tidak sama dengan
        System.out.println(nilai1 != nilai2);






    }
}
